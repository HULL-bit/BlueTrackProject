// Fonctions utilitaires pour les images
export const imageUtils = {
  // Obtenir une image de pirogue aléatoire
  getRandomPirogue: () => {
    const randomIndex = Math.floor(Math.random() * pirogueImages.length);
    return pirogueImages[randomIndex];
  },
  
  // Obtenir une image de pirogue par localisation
  getPirogueByLocation: (location: string) => {
    return pirogueImages.find(img => 
      img.location.toLowerCase().includes(location.toLowerCase())
    ) || pirogueImages[0];
  },
  
  // Générer un placeholder d'image
  generatePlaceholder: (width: number, height: number, text?: string) => {
    const encodedText = text ? encodeURIComponent(text) : 'Image';
    return `https://via.placeholder.com/${width}x${height}/1e40af/ffffff?text=${encodedText}`;
  },
  
  // Optimiser une URL d'image
  optimizeImageUrl: (url: string, width?: number, height?: number, quality?: number) => {
    if (!url) return defaultImages.pirogue;
    
    // Si c'est une image Unsplash, ajouter les paramètres d'optimisation
    if (url.includes('unsplash.com')) {
      const params = new URLSearchParams();
      if (width) params.append('w', width.toString());
      if (height) params.append('h', height.toString());
      if (quality) params.append('q', quality.toString());
      params.append('fit', 'crop');
      
      const separator = url.includes('?') ? '&' : '?';
      return `${url}${separator}${params.toString()}`;
    }
    
    return url;
  },
  
  // Précharger une image
  preloadImage: (url: string): Promise<void> => {
    return new Promise((resolve, reject) => {
      const img = new Image();
      img.onload = () => resolve();
      img.onerror = reject;
      img.src = url;
    });
  },
  
  // Précharger plusieurs images
  preloadImages: async (urls: string[]): Promise<void> => {
    const promises = urls.map(url => imageUtils.preloadImage(url));
    await Promise.all(promises);
  }
};

